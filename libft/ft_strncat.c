#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	if (n == 0)
		return (s1);
	while (s2[i] != '\0')
	{
		if (i < n)
		{
			s1[ft_strlen(s1)] = s2[i];
		}
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
